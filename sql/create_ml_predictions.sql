-- ML Predictions tablosu
CREATE TABLE IF NOT EXISTS ml_predictions (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  match_code VARCHAR(50) NOT NULL REFERENCES matches(match_code),
  market VARCHAR(20) NOT NULL,
  probabilities JSONB NOT NULL,
  predicted_outcome VARCHAR(20),
  confidence FLOAT,
  confident_picks JSONB,
  model_version VARCHAR(50),
  created_at TIMESTAMP DEFAULT NOW(),
  UNIQUE(match_code, market, model_version)
);

-- ML Model Runs tablosu
CREATE TABLE IF NOT EXISTS ml_model_runs (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  model_version VARCHAR(50) UNIQUE NOT NULL,
  trained_at TIMESTAMP DEFAULT NOW(),
  train_size INT,
  test_size INT,
  metrics JSONB,
  feature_count INT,
  notes TEXT
);
